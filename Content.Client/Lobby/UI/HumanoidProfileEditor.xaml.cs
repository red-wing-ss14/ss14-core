                    Profile,
                    _prototypeManager,
                    _resManager,
                    _parsingMan,
                    _controller,
                    species =>
                    {
                        var oldProfile = Profile.Clone();
                        SetSpecies(species);
                        OnSkinColorOnValueChangedKeepColor(oldProfile);
                        UpdateHairPickers();
                        _speciesWindow?.Dispose();
                        _speciesWindow = null;

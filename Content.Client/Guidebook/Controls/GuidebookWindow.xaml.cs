    public void HandleClick(string link)
    {
        if (!_entries.TryGetValue(link, out var entry))
            return;

        if (Tree.TryGetIndexFromMetadata(entry, out var index))
        {
            Tree.ExpandParentEntries(index.Value);
            Tree.SetSelectedIndex(index);
        }
        else
            ShowGuide(entry);
    }

    // Reserve edit start: guide-book #320
    public void NavigateToCrossReferenceTarget(IGuidebookCrossReference reference)
    {
        if (reference.TargetPrototype is not { } target)
            return;

        _crossReferences.TryRevealAndScroll(target, Scroll, UserInterfaceManager);
    }

    private static BoxContainer CreateHeaderRow(string[] titles, float[] widths)
    {
        var row = CreateRowContainer();
        for (var i = 0; i < titles.Length; i++)
        {
            row.AddChild(CreateCell(titles[i], widths[i], true));
        }

        return row;
    }

    private static BoxContainer CreateRowContainer()
    {
        return new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            HorizontalExpand = true,
            SeparationOverride = 8,
        };
    }

    private static Control CreateCell(string text, float width, bool header = false)
    {
        var label = new Label
        {
            Text = text,
            ClipText = true,
            HorizontalExpand = true,
            VerticalAlignment = VAlignment.Center,
            Margin = new Thickness(2, 1),
        };

        if (header)
            label.FontColorOverride = Color.FromHex("#B9B9C2");

        var container = new PanelContainer
        {
            MinWidth = (int) MathF.Max(width > 10 ? width : 80, 80),
            HorizontalExpand = true,
            PanelOverride = new StyleBoxFlat
            {
                BackgroundColor = header ? Color.FromHex("#1F2025") : Color.FromHex("#17181D"),
            },
            SizeFlagsStretchRatio = width > 10 ? 0.01f : width,
        };

        container.AddChild(label);
        return container;
    }

    private static string MaskAccountCode(string accountId)
    {
        return accountId.Length <= 4
            ? "####"
            : $"######{accountId[^4..]}";
    }

    private string LocalizeReason(string reason, string? reasonData)
    {
        var vendingData = ParseVendingPurchaseData(reasonData);
        var cargoOrderData = ParseCargoPrivatePurchaseData(reasonData);
        return reason switch
        {
            "card-deposit" => Loc.GetString("cargo-funding-alloc-console-reason-card-deposit"),
            "card-withdrawal" => Loc.GetString("cargo-funding-alloc-console-reason-card-withdrawal"),
            "payroll" => Loc.GetString("cargo-funding-alloc-console-reason-payroll", ("job", LocalizeJob(reasonData))),
            "starting-payroll" => Loc.GetString("cargo-funding-alloc-console-reason-starting-payroll"),
            "vending-purchase" => Loc.GetString("cargo-funding-alloc-console-reason-vending-purchase", ("item", LocalizeVendingItem(vendingData.ItemId))),
            "cargo-private-purchase" => Loc.GetString("cargo-funding-alloc-console-reason-cargo-private-purchase", ("order", cargoOrderData.OrderId ?? Loc.GetString("cargo-funding-alloc-console-economy-unknown")), ("product", LocalizeCargoProduct(cargoOrderData.ProductId))),
            "cargo-private-purchase-refund" => Loc.GetString("cargo-funding-alloc-console-reason-cargo-private-purchase-refund", ("order", cargoOrderData.OrderId ?? Loc.GetString("cargo-funding-alloc-console-economy-unknown")), ("product", LocalizeCargoProduct(cargoOrderData.ProductId))),
            _ => Loc.GetString("cargo-funding-alloc-console-reason-generic", ("reason", reason)),
        };
    }

    private static (string? ItemId, string? DepartmentId) ParseVendingPurchaseData(string? reasonData)
    {
        if (string.IsNullOrWhiteSpace(reasonData))
            return (null, null);

        var split = reasonData.Split('|', 2, StringSplitOptions.TrimEntries);
        return split.Length == 2 ? (split[0], split[1]) : (reasonData, null);
    }

    private static (string? OrderId, string? ProductId) ParseCargoPrivatePurchaseData(string? reasonData)
    {
        if (string.IsNullOrWhiteSpace(reasonData))
            return (null, null);

        string? orderId = null;
        string? productId = null;
        var entries = reasonData.Split('|', StringSplitOptions.TrimEntries | StringSplitOptions.RemoveEmptyEntries);
        foreach (var entry in entries)
        {
            var pair = entry.Split(':', 2, StringSplitOptions.TrimEntries);
            if (pair.Length != 2)
                continue;

            switch (pair[0])
            {
                case "order":
                    orderId = pair[1];
                    break;
                case "product":
                    productId = pair[1];
                    break;
            }
        }

        return (orderId, productId);
    }

    private string LocalizeVendingItem(string? itemId)
    {
        if (string.IsNullOrWhiteSpace(itemId))
            return Loc.GetString("cargo-funding-alloc-console-economy-unknown");

        if (_prototypeManager.TryIndex<EntityPrototype>(itemId, out var proto))
            return Loc.GetString(proto.Name);

        return Loc.GetString("cargo-funding-alloc-console-economy-unknown");
    }

    private string LocalizeCargoProduct(string? productId)
    {
        if (string.IsNullOrWhiteSpace(productId))
            return Loc.GetString("cargo-funding-alloc-console-economy-unknown");

        if (_prototypeManager.TryIndex<EntityPrototype>(productId, out var proto))
            return Loc.GetString(proto.Name);

        return Loc.GetString("cargo-funding-alloc-console-economy-unknown");
    }

    private string LocalizeJob(string? jobId)
    {
        if (string.IsNullOrWhiteSpace(jobId))
            return Loc.GetString("cargo-funding-alloc-console-economy-unknown");

        if (_prototypeManager.TryIndex<JobPrototype>(jobId, out var job))
            return Loc.GetString(job.Name);

        return Loc.GetString("cargo-funding-alloc-console-economy-unknown");
    }

    private string LocalizeDepartmentCode(string? departmentId)
    {
        if (string.IsNullOrWhiteSpace(departmentId))
            return Loc.GetString("cargo-funding-alloc-console-economy-unknown");

        if (_prototypeManager.TryIndex<CargoAccountPrototype>(departmentId, out var proto))
            return Loc.GetString(proto.Code);

        return Loc.GetString("cargo-funding-alloc-console-economy-unknown");
    }

    private static string FormatDuration(TimeSpan time)
    {
        return $"{(int) time.TotalHours:D2}:{time.Minutes:D2}:{time.Seconds:D2}";
    }

    private void RefreshUi()
    {
        if (_station == null)
            return;

        BuildEntries();
        UpdateButtonDisabled();
    }

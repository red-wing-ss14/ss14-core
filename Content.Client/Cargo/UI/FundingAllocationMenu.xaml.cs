    private void BuildEconomy(FundingAllocationConsoleBuiState state)
    {
        EconomyAccountsContainer.RemoveAllChildren();
        TransactionsContainer.RemoveAllChildren();
        _economyAccountControls.Clear();
        _transactionControls.Clear();

        BuildEconomyHeaders();

        var accountsByEntity = state.EconomyAccounts.ToDictionary(a => a.Account);

        NoAccountsLabel.Visible = state.EconomyAccounts.Count == 0;
        foreach (var account in state.EconomyAccounts.OrderBy(a => a.AccountId))
        {
            var roleText = string.IsNullOrWhiteSpace(account.JobId)
                ? Loc.GetString("cargo-funding-alloc-console-economy-role-none")
                : LocalizeJob(account.JobId);

            if (string.IsNullOrWhiteSpace(roleText))
                roleText = Loc.GetString("cargo-funding-alloc-console-economy-role-none");

            var row = CreateRowContainer();
            row.AddChild(CreateCell(MaskAccountCode(account.AccountId), 130f));
            row.AddChild(CreateCell(account.AccountName, 1f));
            row.AddChild(CreateCell(roleText, 1f));
            row.AddChild(CreateCell(Loc.GetString("cargo-funding-alloc-console-money", ("amount", account.Balance)), 130f));

            EconomyAccountsContainer.AddChild(row);
            _economyAccountControls.Add(row);
        }

        NoTransactionsLabel.Visible = state.Transactions.Count == 0;
        foreach (var transaction in state.Transactions.OrderByDescending(t => t.Time).ThenByDescending(t => t.Index))
        {
            accountsByEntity.TryGetValue(transaction.Account, out var mainAccount);
            accountsByEntity.TryGetValue(transaction.Counterparty ?? default, out var counterpartyAccount);

            var incoming = transaction.Delta > 0;
            var operationType = incoming
                ? Loc.GetString("cargo-funding-alloc-console-economy-operation-income")
                : Loc.GetString("cargo-funding-alloc-console-economy-operation-expense");

            var unknownText = Loc.GetString("cargo-funding-alloc-console-economy-unknown");
            var from = incoming
                ? counterpartyAccount?.AccountName ?? unknownText
                : mainAccount?.AccountName ?? unknownText;
            var to = incoming
                ? mainAccount?.AccountName ?? unknownText
                : counterpartyAccount?.AccountName ?? unknownText;

            switch (incoming)
            {
                case true when transaction.Reason == "payroll":
                    from = LocalizeDepartmentCode(mainAccount?.DepartmentId);
                    break;
                case false when transaction.Reason == "vending-purchase":
                    to = LocalizeDepartmentCode(ParseVendingPurchaseData(transaction.ReasonData).DepartmentId);
                    break;
                case false when transaction.Reason == "cargo-private-purchase":
                    to = LocalizeDepartmentCode("Cargo");
                    break;
                case true when transaction.Reason == "cargo-private-purchase-refund":
                    from = LocalizeDepartmentCode("Cargo");
                    break;
            }

            var row = CreateRowContainer();
            row.AddChild(CreateCell(operationType, 110f));
            row.AddChild(CreateCell(Loc.GetString("cargo-funding-alloc-console-money", ("amount", transaction.Delta)), 110f));
            row.AddChild(CreateCell(from, 1f));
            row.AddChild(CreateCell(to, 1f));
            row.AddChild(CreateCell(LocalizeReason(transaction.Reason, transaction.ReasonData), 2f));
            row.AddChild(CreateCell(FormatDuration(transaction.Time), 90f));

            TransactionsContainer.AddChild(row);
            _transactionControls.Add(row);
        }
    }



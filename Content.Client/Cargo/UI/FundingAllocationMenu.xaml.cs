    // Orion-Edit-Start
    private void UpdateButtonDisabled()
    {
        if (!_entityManager.TryGetComponent<StationBankAccountComponent>(_station, out var bank))
            return;

        var sum = _spinBoxes.Sum(s => s.Value);
        var incorrectSum = sum != 100;

        var differs = false;
        var revenueDistribution = bank.RevenueDistribution;
        var accounts = EditableAccounts(bank)
            .OrderBy(p => p.Key)
            .Select(p => p.Key)
            .ToList();

        for (var i = 0; i < accounts.Count; i++)
        {
            var currentPercent = _spinBoxes[i].Value;
            var expectedPercent = (int) Math.Round((revenueDistribution.TryGetValue(accounts[i], out var pct) ? pct : 0) * 100);

            if (currentPercent == expectedPercent)
                continue;

            differs = true;
            break;
        }

        differs = differs || _primaryCut != bank.PrimaryCut || _lockboxCut != bank.LockboxCut;

        SaveButton.Disabled = !differs || incorrectSum;

        var diff = sum - 100;
        SaveAlertLabel.Visible = incorrectSum;
        SaveAlertLabel.SetMarkup(Loc.GetString(
            "cargo-funding-alloc-console-label-save-fail",
            ("pos", Math.Sign(diff)),
            ("val", Math.Abs(diff))));
    }
    // Orion-Edit-End

    public void Update(FundingAllocationConsoleBuiState state)
    {
        _station = _entityManager.GetEntity(state.Station);

        // Orion-Edit-Start
        if (ShouldRebuildEntries())
            BuildEntries();
        // Orion-Edit-End

        BuildEconomy(state); // Orion
        UpdateButtonDisabled();
    }

    // Orion-Start
    private bool ShouldRebuildEntries()
    {
        if (_station == null || !_entityManager.TryGetComponent<StationBankAccountComponent>(_station, out var bank))
            return false;

        var accounts = EditableAccounts(bank)
            .OrderBy(p => p.Key)
            .Select(p => p.Key)
            .ToList();

        if (_entriesStation != _station || _entriesAccounts.Count != accounts.Count)
        {
            _entriesStation = _station;
            _entriesAccounts = accounts;
            return true;
        }

        for (var i = 0; i < accounts.Count; i++)
        {
            if (_entriesAccounts[i].Equals(accounts[i]))
                continue;

            _entriesAccounts = accounts;
            return true;
        }

        return false;
    }
    // Orion-End

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);

        if (!_bankQuery.TryComp(_station, out var bank))
            return;

        foreach (var (account, label) in _balanceLabels)
        {
            label.Text = Loc.GetString("cargo-console-menu-points-amount", ("amount", bank.Accounts[account]));
        }
    }
